package aulas.aula002;

public class Calculadora {
    
    Double numero1;
    Double numero2;

    Double somar(){
        return numero1 + numero2;
    }

    Double subtrair(){
        return numero1 - numero2;
    }

    Double multiplicar(){
        return numero1 * numero2;
    }

    Double dividir(){
        return numero1 / numero2;
    }
}
