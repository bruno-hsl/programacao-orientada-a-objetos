package livraria;

public class Livro {

    int id;
    String titulo;
    String nomeAutor;
    String anoPublicao;
    String editora;

        public Livro(int id, String titulo, String nomeAutor, String anoPublicacao, String editora){
            this.id = id;
            this.titulo = titulo;
            this.nomeAutor = nomeAutor;
            this.anoPublicao = anoPublicacao;
            this.editora = editora;
        }
    
}
